    private void jbtnLogActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jbtnLogActionPerformed
        statusMessageLabel.setText("");
        ArrayList<String> hist = a.getLog();
        if (!a.getErrMsg().isEmpty()) {
            statusMessageLabel.setText(a.getErrMsg());
            return;
        } else {
            statusMessageLabel.setText(a.getActionMsg());
        }
        JTextArea t = new JTextArea();
        for (int i = 0; i < hist.size(); i++) {
            t.append(hist.get(i) + "\n");
        }
        JScrollPane sp = new JScrollPane(t);
        JDialog dg = new JDialog();
        dg.add(sp);
        dg.setTitle("History for Account: " + a.getAcctName());
        dg.setBounds(150, 400, 600, 300);
        dg.setVisible(true);
    }//GEN-LAST:event_jbtnLogActionPerformed

    private void jmnuOpenActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jmnuOpenActionPerformed
        int accountNo;
        String fileName, typeCode;
        statusMessageLabel.setText("");
        setInputLine(false);
        
        JFileChooser f = new JFileChooser(".");
        f.setDialogTitle("Select Account File");
        FileNameExtensionFilter filter = 
                new FileNameExtensionFilter("Text Files (*.txt)", "txt");
        f.setFileFilter(filter);
        JDialog dg = new JDialog();
        int returnValue = f.showOpenDialog(dg);
        
        if (returnValue == JFileChooser.CANCEL_OPTION) {
            statusMessageLabel.setText("Open action canceled");
        } else {
            fileName = f.getSelectedFile().getName();
            typeCode = fileName.substring(0,2).toUpperCase();
            if (fileName.substring(2,3).equalsIgnoreCase("L")) {
                accountNo = Integer.parseInt(fileName.substring(3).replace(".txt",""));
            } else {
                accountNo = Integer.parseInt(fileName.substring(2).replace(".txt",""));

            }
            switch (typeCode) {
                case CreditCard.TypeCd:
                    a = new CreditCard(accountNo);
                    break;
                case Checking.TypeCd:
                    a = new Checking(accountNo);
                    break;
                case EquityLine.TypeCd:
                    a = new EquityLine(accountNo);
                    break;
                case Savings.TypeCd:
                    a = new Savings(accountNo);
                    break;
                default:
                    statusMessageLabel.setText("unknown account type.");
                    return;
            }
            if (a.getErrMsg().isEmpty()) {
                DisplayValues();
                statusMessageLabel.setText(a.getActionMsg());
            } else {
                statusMessageLabel.setText(a.getErrMsg());
            }
        }
    }//GEN-LAST:event_jmnuOpenActionPerformed

    private void DisplayValues() {
        NumberFormat c = NumberFormat.getCurrencyInstance();
        jtxtDisplayName.setText(a.getAcctName());
        jtxtAcctNo.setText(String.valueOf(a.getAcctNo()));
        jtxtAcctTypeDesc.setText(a.getAcctTypeDesc());
        jtxtBalance.setText(c.format(a.getBalance()));
        
        if (a instanceof AssetAccount) {
            jlblAvailable.setVisible(false);
            jtxtAvailable.setVisible(false);
            jlblCLimit.setVisible(false);
            jtxtCLimit.setVisible(false);
            jlblEqVal.setVisible(false);
            jtxtEqVal.setVisible(false);
        } else if (a instanceof CreditAccount) {
            jlblAvailable.setVisible(true);
            jtxtAvailable.setVisible(true);
            jlblCLimit.setVisible(true);
            jtxtCLimit.setVisible(true);
            jlblEqVal.setVisible(true);
            jtxtEqVal.setVisible(true);
            CreditAccount cr = (CreditAccount) a;
            jtxtCLimit.setText(c.format(cr.getLimit()));
            jtxtAvailable.setText(c.format(cr.getAvailCr()));
            jtxtEqVal.setText(c.format(cr.getEquityValue()));
        }
    }

