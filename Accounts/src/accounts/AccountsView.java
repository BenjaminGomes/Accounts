    private void jbtnCreateActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jbtnCreateActionPerformed
        double startValue;
        try {
            startValue = Double.parseDouble(jtxtStartVal.getText());
        } catch (NumberFormatException e) {
            startValue = 0;
        }
        switch (jtxtTypeCd.getText()) {
            case Checking.TypeCd:
                a = new Checking(jtxtAcctNm.getText(), startValue);
                break;
            case Savings.TypeCd:
                a = new Savings(jtxtAcctNm.getText(), startValue);
                break;
            case CreditCard.TypeCd:
                a = new CreditCard(jtxtAcctNm.getText(), startValue);
                break;
            case EquityLine.TypeCd:
                a = new EquityLine(jtxtAcctNm.getText(), startValue);
                break;
        } // end of switch
        if (a.getErrMsg().isEmpty()) {
            DisplayValues();
            statusMessageLabel.setText(a.getActionMsg());
            jtxtChgAmt.requestFocusInWindow();
        } else {
            statusMessageLabel.setText(a.getErrMsg());
        }
        setInputLine(false);
    }//GEN-LAST:event_jbtnCreateActionPerformed

